import { networkInterfaces } from 'node:os';
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';

const preferredNames = ['en0', 'en1', 'bridge100'];
const privateIp = /^(192\.168\.|10\.|172\.(1[6-9]|2\d|3[0-1])\.)/;

function collectCandidates() {
  const nets = networkInterfaces();
  const candidates = [];

  for (const [name, addresses] of Object.entries(nets)) {
    for (const address of addresses || []) {
      if (address.family !== 'IPv4' || address.internal) {
        continue;
      }
      if (!privateIp.test(address.address)) {
        continue;
      }
      candidates.push({
        name,
        address: address.address,
        score: preferredNames.includes(name) ? preferredNames.indexOf(name) : 99,
      });
    }
  }

  return candidates.sort((a, b) => a.score - b.score);
}

const manualIp = process.env.MINI_API_HOST;
const manualApiBase = process.env.MINI_API_BASE;
const manualWsUrl = process.env.MINI_WS_URL;
const apiPort = process.env.MINI_API_PORT || '18080';
const selectedIp = manualIp || collectCandidates()[0]?.address || '127.0.0.1';
const apiBase = manualApiBase || `http://${selectedIp}:${apiPort}`;
const wsUrl = manualWsUrl || (apiBase.startsWith('https://')
  ? apiBase.replace(/^https:\/\//, 'wss://') + '/ws'
  : `ws://${selectedIp}:${apiPort}/ws`);
const target = resolve('src/config/local-dev.ts');

const content = `// Auto-generated by scripts/write-local-env.mjs.
// Re-run npm run prepare:local when your Wi-Fi IP changes.
export const localDevConfig = {
  apiBase: '${apiBase}',
  ieBase: '${apiBase}',
  wsUrl: '${wsUrl}',
};
`;

mkdirSync(dirname(target), { recursive: true });
writeFileSync(target, content, 'utf8');

console.log(`[mini-program] local API base: ${apiBase}`);
